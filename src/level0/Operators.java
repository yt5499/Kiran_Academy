package level0;

public class Operators {

	public static void main(String[] args) {
		byte a = 25;
		byte b = 5;
		
//		Arithmetic operators
		System.out.println("Arithmetic operators");
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a/b);
		System.out.println(a*b);
		System.out.println(a%b);
		System.out.println();
		
//		Relational operator
		System.out.println("Relational operator");
		System.out.println(a<b);
		System.out.println(a<=b);
		System.out.println(a>b);
		
		
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
	}

}
